				<h2> Préparation</h2>
				<p>
					<h3>Étape 1</h3>
				Coupez le poisson en dès d'environ 0.5 cm de coté et faîtes-le tremper dans de l'eau salée.
				</p>
				<p>
					<h3>Étape 2</h3>
				Pendant ce temps, découpez les concombres, les tomates en dès et râpez les carottes, essorez tous les légumes et mélangez (important).
				</p>
				<p>
					<h3>Étape 3</h3>
				Videz l'eau où se trouve le poisson et ajoutez à la place le litre de jus de citron.
				</p>
				<p>
					<h3>Étape 4</h3>
				Pendant ce temps-là, hachez le gingembre et coupez en rondelles fines les oignons verts.
				</p>
				<p>
					<h3>Étape 5</h3>
				Vérifiez la cuisson du poisson dans le citron, si la chair a blanchi alors le poisson est cuit.
				</p>
				<p>
					<h3>Étape 6</h3>
				Videz alors tout le jus de citron et ajoutez tous les légumes, mélangez le tout. Salez, poivrez et ajoutez le gingembre.	
				</p>
				<p>
					<h3>Étape 7</h3>
				Versez le lait de coco sur la salade et décorez d'oignons verts.
				</p>


